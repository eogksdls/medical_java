package p0621;

public class Cal2 {
	
	int cal(int a, int b) {
		int result = a + b;
		int result2 = a - b;
		int result3 = a * b;
		int result4 = a / b;
		
		return result; // return을 1개밖에 하지 못해... 
	}
	
	
	
	int plus(int a, int b) {
		return a+b;
	}
	
	int minus(int a, int b) {
		return a-b;
	}
	
	int multi(int a, int b) {
		return a*b;
		
	}
	
	double divide(double a, double b) {
		return a / b;
	}


}
