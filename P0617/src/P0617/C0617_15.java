package P0617;

public class C0617_15 {
	public static void main(String[] args) {
		// for 문
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		
		// do, while 문
		int j=0; 
		
		do {
			j++;
			System.out.println(j);
		}while(j<=5);  // j 가 5 이하일때까지 반복문 돌리기(조건문)
		
		// while 문(조건문)
		int k=0;
		while(k<=5) {
			k++;
			System.out.println(k);
		}
	}
}
